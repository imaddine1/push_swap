int	ft_isalnum(int c)
{
	unsigned char	num;

	num = (unsigned char)c;
	if ((num >= '0' && num <= '9') || (num >= 'a' && num <= 'z')
		|| (num >= 'A' && num <= 'Z'))
		return (1);
	return (0);
}
